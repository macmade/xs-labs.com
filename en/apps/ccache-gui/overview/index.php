<p>
    <strong>A GUI helper application for the ccache tool.</strong>
</p>
<p>
    ccache can be installed on macOS with Homebrew:<br />
    <code>brew install ccache</code>
</p>
<p>
    The application run by default in the menu bar, but the window can be detached for convenience.
</p>
