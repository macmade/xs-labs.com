<p>
    FileSystem is an application that allows you to browse the content of your iPhone disk, displaying file and folders, files contents, and detailed informations about file and folder permissions.<br />
    No file editing nor folder creation can be done through this application. <br />
    It is intended to developers, to learn about the internal organization of the iOS file system.
</p>
<h2>Compatibility</h2>
<p>
    FileSystem is an universal application, meaning you can use it on iPhone, iPod Touch and iPad.<br />
    It requires at least iOS 4.0.<br />
    Latest versions of iOS, as well as latest iOS devices (iPhone 5, iPad Mini) are fully supported.
</p>
<h2>iOS 7 Support</h2>
<p>
    FileSystem is fully compatible with iOS 7.
</p>
