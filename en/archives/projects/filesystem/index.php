<h3>Compatibility</h3>
<p>
    FileSystem is an universal application, meaning you can use it on iPhone, iPod Touch and iPad.<br />
    It requires at least iOS 4.0.<br />
    Latest versions of iOS, as well as latest iOS devices (iPhone 5, iPad Mini) are fully supported.
</p>
<h3>iOS 7 Support</h3>
<p>
    FileSystem is fully compatible with iOS 7.
</p>
<h3>AppStore Version</h3>
<p>
    FileSystem used to be available on the AppStore.<br />
    Unfortunately, after a few years of availability, Apple decided to <a href="http://www.noxeos.com/2012/05/27/apple-kills-filesystem-app/">remove it</a>.<br />
    The complete source code is now <a href="https://github.com/macmade/FileSystem">available freely on GitHub</a>.
</p>
