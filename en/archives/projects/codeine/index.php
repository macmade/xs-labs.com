<p>
    Codeine is a new code editor for Mac, allowing editing, building, running and debugging C, C++ and Objective-C code.
</p>
<p>
    While not an IDE (yet), Codeine aims to evolve to support complex application projects.
</p>
<p>
    Codeine uses the latest technologies in source code compilation to provide users with the best environment to build and run software on the Mac platform.
</p>
<p>
    <a href="https://github.com/macmade/Codeine" class="btn btn-primary" role="button">View source on GitHub</a>
</p>
<h3>Highlights</h3>
<div class="row">
    <div class="col-sm-6">
        <div class="panel panel-default">
            <div class="panel-heading">
                <h3 class="project-icons project-icons-design-blueprint">
                    Easy to use - yet powerfull
                </h3>
            </div>
            <div class="panel-body">
                <p>
                    <img src="/uploads/image/codeine/codeine.png" alt="Codeine" width="100" height="100" class="pull-right" />
                    Wether you're an experienced developer or not, Codeine will provide you with all the tools you need, when you need them. It has been designed with simplicity and clearness in mind, but also lets you take a full control over your development toolchain. Beginners will like it's simplicity while experienced developers will enjoy its completeness.
                 </p>
            </div>
        </div>
    </div>
    <div class="col-sm-6">
        <div class="panel panel-default">
            <div class="panel-heading">
                <h3 class="project-icons project-icons-gear">
                    Clang/LLVM under the hood
                </h3>
            </div>
            <div class="panel-body">
                <p>
                    <img src="/uploads/image/codeine/llvm.png" alt="Codeine" width="100" height="100" class="pull-right" />
                    Codeine makes an extensive use of Clang/LLVM, the next-generation compiler technology. While it's obviously used to compile your source files, Codeine also uses it to provide syntax highlighting, code completion, diagnostics and fix-it suggestions, providing you with the most accurate informations at editing and build time.
                </p>
            </div>
        </div>
    </div>
</div>
<div class="row">
    <div class="col-sm-6">
        <div class="panel panel-default">
            <div class="panel-heading">
                <h3 class="project-icons project-icons-box">
                    Full featured toolchain
                </h3>
            </div>
            <div class="panel-body">
                <p>
                    <img src="/uploads/image/codeine/tools.png" alt="Codeine" width="100" height="100" class="pull-right" />
                    Codeine is not just a text editor. It comes with a complete compiler, linker and debugger toolchain, built from the latest versions of Clang/LLVM. Its settings let you easily configure the way your software is built, by providing a full control over the compiler and linker settings through an easy to use interface.
                 </p>
            </div>
        </div>
    </div>
    <div class="col-sm-6">
        <div class="panel panel-default">
            <div class="panel-heading">
                <h3 class="project-icons project-icons-notification">
                    Live diagnostics
                </h3>
            </div>
            <div class="panel-body">
                <p>
                    <img src="/uploads/image/codeine/note.png" alt="Codeine" width="100" height="100" class="pull-right" />
                    Thanks to the power of Clang/LLVM, Codeine provides live code diagostics. Compiler errors and warnings are displayed as you type, with extended diagnostic informations, making them easy to spot and fix. It even provides you with automatic and contextual fixes suggestions, to help you in the debugging process.
                </p>
            </div>
        </div>
    </div>
</div>
