<h3>Cookies</h3>
<p>
    A cookie is a string of information that a website stores on a visitor's computer, and that the visitor's browser provides to the website each time the visitor returns. XS-Labs uses cookies to help XS-Labs identify and track visitors, their usage of XS-Labs website, and their website access preferences. XS-Labs visitors who do not wish to have cookies placed on their computers should set their browsers to refuse cookies before using XS-Labs's websites, with the drawback that certain features of XS-Labs's websites may not function properly without the aid of cookies.
</p>
<h3>Ads</h3>
<p>
    Ads appearing on any of our websites may be delivered to users by advertising partners, who may set cookies. These cookies allow the ad server to recognize your computer each time they send you an online advertisement to compile information about you or others who use your computer. This information allows ad networks to, among other things, deliver targeted advertisements that they believe will be of most interest to you. This Privacy Policy covers the use of cookies by XS-Labs and does not cover the use of cookies by any advertisers.
